      minZoom: 0,
      maxZoom: maxZoom,
      renderWorldCopies: false,
      attributionControl: false,
    })

    // 🎬 Map Events
    map.on('load', () => {
      console.log('✅ Karte geladen!')

      // Drawing System initialisieren
      initializeDrawing()

      // Enhanced Drawing Events
      setupEnhancedDrawingEvents()

      // Drawing Event Listeners für Sidebar
