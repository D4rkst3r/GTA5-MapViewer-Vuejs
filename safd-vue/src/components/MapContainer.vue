// 🎛️ Layer Toggle Management
const toggleLayer = (layerName, visible) => {
  if (map && map.getLayer(layerName)) {
    map.setLayoutProperty(layerName, 'visibility', visible ? 'visible' : 'none')
  }
}

// 👀 Watchers
