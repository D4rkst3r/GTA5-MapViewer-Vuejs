const refreshHydrantLayer = () => {
  if (map.getSource('hydrants')) {
    const features = hydrantsStore.hydrants.map((hydrant) => ({
      type: 'Feature',
      geometry: {
        type: 'Point',
        coordinates: [hydrant.lng, hydrant.lat],
      },
      properties: hydrant,
    }))

    map.getSource('hydrants').setData({
      type: 'FeatureCollection',
      features,
    })
  }
}

