  () => hydrantsStore.hydrants,
  () => {
    refreshHydrantLayer()
  },
  { deep: true },
)

watch(
  () => poiStore.pois,
  () => {
    refreshPOILayer()
  },
  { deep: true },
)

watch(
  () => markersStore.markers,
  () => {
    refreshMarkersLayer()
  },
  { deep: true },
)

watch(
  () => mapStore.layers,
  (newLayers) => {
    Object.keys(newLayers).forEach((layerName) => {
      const mapLayerName =
        layerName === 'hydrants'
          ? 'hydrants-layer'
          : layerName === 'pois'
            ? 'pois-layer'
            : layerName === 'markers'
              ? 'markers-layer'
              : layerName
      toggleLayer(mapLayerName, newLayers[layerName])
    })
  },
  { deep: true },
)

watch(
  () => mapStore.mapStyle,
  (newStyle) => {
    if (map && newStyle) {
      // Switch map style
      const layers = ['road-layer', 'satellite-layer', 'atlas-layer']
      layers.forEach((layer) => {
        if (map.getLayer(layer)) {
          map.setLayoutProperty(layer, 'visibility', 'none')
        }
      })

      const targetLayer = `${newStyle}-layer`
      if (map.getLayer(targetLayer)) {
        map.setLayoutProperty(targetLayer, 'visibility', 'visible')
      }
    }
  },
)

